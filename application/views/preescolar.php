					<h1 class="margin-bottom30 purple2 shadow text-center"><img src="assets/images/Preescolar.png"> Preescolar</h1>
					<p>Nuestro método pedagógico educativo en la educación preescolar esta enfocado a garantizar el desarrollo psicológico armonioso de nuestros niños y niñas. Este Método se basa en la concepción del desarrollo histórico cultural de la psique humana propuesto por L. S. Vigostky y sus seguidores en psicología. De acuerdo a este enfoque el desarrollo transcurre en las actividades culturales motivadas. Dichas actividades son colectivas, sociales y externas en el momento inicial del desarrollo y más adelante se consolidan como el plano interno, individual de la actividad y personalidad de cada niño. </p>

					<p>Este aporte curricular tiene como objetivo propiciar actividades colaborativas colectivas orientadas por parte del adulto de manera constante a través de medios del lenguaje, juegos, acciones cotidianas compartidas y comunicación reflexiva positiva personal.</p>
				</div>
