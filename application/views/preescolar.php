				</div>
				<div class="col-12 col-sm-12 col-md-6 col-lg-6 col-xl-6">
					<h2 class="purple2 shadow">Objetivo general:</h2>
					<p>Garantizar en el alumno un nivel óptimo de preparación psicológica para el inicio del aprendizaje escolar, a través de la formación de los siguientes aspectos psicológicos nuevos que aparecen al final del periodo preescolar:</p>
					<p>• Un comportamiento voluntario, el respeto de reglas, las normas y la iniciativa reflexiva que el niño proponga objetivos propios en diversas actividades.<br>
					• Una actitud reflexiva en relación con diversos aspectos comunicativos, dirigidos a otra persona (niños y adultos), dentro de relaciones sociales amplias y positivas.<br>
					• La imaginación, que incluye la anticipación de secuencias de comportamientos sociales propios y ajenos, así como la posibilidad de producir y compartir diversas situaciones imaginarias en las actividades lúdicas y creativas.<br>
					• Desarrollar los aspectos positivos en la esfera de la personalidad y la motivación hacia el intercambio social, que incluye la compasión, la empatía, el respeto, la comprensión y la habilidad de brindar ayudas mutuas en diversos sucesos de la vida cotidiana, obras artísticas y literarias (películas, cuentos, obras artísticas accesibles) y la actividad lúdica.<br>
					• Formar Intereses y motivaciones amplias hacia la cognición y el futuro aprendizaje sistematizado que se manifiesta en la curiosidad y en una actitud activa y positiva hacia diversas informaciones que caracterizan la vida en la sociedad humana.</p>
				</div>
				<div class="col-12 col-sm-12 col-md-6 col-lg-6 col-xl-6">
					<h2 class="purple2 shadow">Estrategias pedagógicas (actividades básicas):</h2>
					<p>• Inclusión participativa de cada uno de los niños en diversas formas de la actividad lúdica. Todas ellas son específicas para cada grado y de acuerdo la actividad rectora del periodo preescolar. <br>
					• Introducción gradual formativa en la actividad de dibujo infantil a través de la orientación especifica elaborada para cada grado del nivel preescolar.<br>
					• Presentación colectiva desplegada de contenidos del lenguaje literario a través de lectura orientativa y el análisis desplegado de los contenidos de los textos literarios (cuentos mágicos, descriptivos, infantiles y fábulas), elegidos específicamente para cada grado del nivel preescolar.<br>
					• Inclusión de los niños en actividades motrices de educación física a través de estrategias lúdicas motivadoras para cada grado del nivel preescolar.<br>
					• A partir del segundo grado de la educación preescolar se garantiza la inclusión de los niños en el conocimiento inicial del vocabulario oral expresivo del idioma inglés mediante acciones desplegadas con objetos de la vida cotidiana y situaciones comunicativas cotidianas a través del uso constante de medios expresivos no verbales como juguetes y objetos.</p>
