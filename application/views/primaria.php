					• Promover en los alumnos la participación reflexiva en las actividades colectivas que presuponen el uso de conceptos de las materias escolares en acciones intelectuales especificas (propias de cada área científica).<br>
					• Formar en los alumnos intereses cognoscitivos reflexivos, en relación con los sistemas de conceptos y acciones intelectuales de las materias escolares.<br>
					• Formar la reflexión teórica para valorar los diversos sucesos del mundo real, incluyendo la esfera artística, las relaciones cotidianas y el medio ambiente.</p>
