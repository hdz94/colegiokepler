					<h1 class="margin-bottom30 blue2 shadow text-center"><img src="assets/images/primaria.png"> Primaria</h1>
					<p>Nuestro método pedagógico educativo en la escuela primaria se basa en la teoría de la actividad aplicada a la enseñanza-aprendizaje. El concepto psicológico de esta actividad la considera como un proceso colectivo desplegado que incluye a dos participantes: el maestro y el niño. El papel del maestro consiste en garantizar, a través de orientaciones específicas, la adquisición de conocimientos conceptuales en los niños. La actividad de enseñanza-aprendizaje comprende la participación activa y constante de todos los niños del grupo bajo la orientación del maestro. </p>
					<p> La organización de la actividad de enseñanza-aprendizaje implica la organización de sistemas de conceptos de las materias escolares de lo general hacia lo particular. Primeramente, se trabaja sobre la adquisición de conceptos iniciales más generales de cada una de las materias escolares. La adquisición se logra a través de la exteriorización del contenido de cada concepto y su inclusión en las actividades externas materializadas grupales. En todas estas actividades se determina específicamente la parte de la orientación de cada tarea escolar particular. Inicialmente dichas tareas necesariamente son grupales y más adelante se convierten en acciones psicológicas independientes de los alumnos. </p>
					<p>Durante el trabajo grupal de cada sesión en la clase, el pedagogo promueve permanentemente la conversación dialógica, con el objeto de incluir a todos los alumnos en el proceso de la comprensión y la reflexión constante acerca de los conceptos científicos generales y las acciones intelectuales que corresponden a ellos para cada materia escolar.</p>
